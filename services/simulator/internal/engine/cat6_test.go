func TestCat6_Quick(t *testing.T) {
	declaredRTPs := []float64{0.902, 0.930, 0.960, 0.905, 0.935, 0.965}
	labels := []string{"R90", "R93", "R96", "R90_Buy", "R93_Buy", "R96_Buy"}

	for mode := 0; mode < 6; mode++ {
		s := cat6Schema(mode)
		if err := s.Validate(); err != nil {
			t.Fatalf("mode %d schema invalid: %v", mode, err)
		}
		cfg := schema.SimulationConfig{SpinCount: 1_000_000, Rows: 5, Seed: uint64(mode + 1)}
		if err := cfg.Validate(); err != nil {
			t.Fatalf("mode %d config invalid: %v", mode, err)
		}

		res, err := Run(s, cfg)
		if err != nil {
			t.Fatalf("mode %d simulation error: %v", mode, err)
		}

		delta := res.RTP - declaredRTPs[mode]
		pass := delta >= -0.005 && delta <= 0.005 // wider tolerance for 1M spins
		fmt.Printf("Quick mode %d (%s): RTP=%.4f declared=%.4f delta=%+.4f BaseRTP=%.4f FreeRTP=%.4f Triggers=%d %s\n",
			mode, labels[mode], res.RTP, declaredRTPs[mode], delta,
			res.BaseRTP, res.FeatureRTP.FreeSpins, res.FeatureTriggerCount,
			map[bool]string{true: "PASS", false: "FAIL"}[pass])
	}
}

func TestCat6_AllModes(t *testing.T) {
	declaredRTPs := []float64{0.902, 0.930, 0.960, 0.905, 0.935, 0.965}
	labels := []string{"R90", "R93", "R96", "R90_Buy", "R93_Buy", "R96_Buy"}

	for mode := 0; mode < 6; mode++ {
		s := cat6Schema(mode)
		if err := s.Validate(); err != nil {
			t.Fatalf("mode %d schema invalid: %v", mode, err)
		}
		cfg := schema.SimulationConfig{SpinCount: 10_000_000, Rows: 5, Seed: uint64(mode + 1)}
		if err := cfg.Validate(); err != nil {
			t.Fatalf("mode %d config invalid: %v", mode, err)
		}

		res, err := Run(s, cfg)
		if err != nil {
			t.Fatalf("mode %d simulation error: %v", mode, err)
		}

		delta := res.RTP - declaredRTPs[mode]
		pass := delta >= -0.001 && delta <= 0.001
		fmt.Printf("Mode %d (%s): RTP=%.4f declared=%.4f delta=%.4f %s\n",
			mode, labels[mode], res.RTP, declaredRTPs[mode], delta,
			map[bool]string{true: "PASS", false: "FAIL"}[pass])

		if !pass {
			t.Errorf("mode %d (%s): RTP=%.6f deviates from declared %.6f by %.6f (tolerance ±0.001)",
				mode, labels[mode], res.RTP, declaredRTPs[mode], delta)
		}
	}
}
